    private void userAccounts_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userAccounts_tableMouseClicked
        // TODO add your handling code here:
        String role ="";
        try {
            int row = userAccounts_table.getSelectedRow();
            String Table_click = (userAccounts_table.getModel().getValueAt(row, 0).toString());
            String sql = "select * from users where UserID ='" + Table_click + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                String stID = rs.getString("UserID");
                userAccountID_txt.setText(stID);
                role = rs.getString("Type_ID");
                String gdname = rs.getString("Username");
                userAccountName_txt.setText(gdname);
                String gdsname = rs.getString("Password");
                userAccountPassword_txt.setText(gdsname);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        try{
            String query ="select * from user_types where Type_ID ='"+role+"' ";
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
            if(rs.next()){
                String result = rs.getString("Type_name");
                userTypeID_combo.setSelectedItem(result);
            }	    
	}catch(Exception e){
	JOptionPane.showMessageDialog(null, e);
	}
    }//GEN-LAST:event_userAccounts_tableMouseClicked

