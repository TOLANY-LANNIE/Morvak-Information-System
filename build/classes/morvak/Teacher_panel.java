    public String idUpdater(String input){
        char [] chars = input.toCharArray();
        int num =Character.getNumericValue(chars[chars.length-1]);
        if(num<=8){
            num++;
            char c = (char)(num+'0');
            chars[chars.length-1] =c;
        }
        else if(num==9){
            int num1 =Character.getNumericValue(chars[chars.length-2]);//takes the preceedig value
            if(num1<=8){
            num1++;
            char c = (char)(num1+'0');
            chars[chars.length-2] =c;
            chars[chars.length-1] ='0';
            }
			else if(num1==9){
            int num2 =Character.getNumericValue(chars[chars.length-3]);//takes the preceedig value
            if(num2<=8){
            num2++;
            char c = (char)(num2+'0');
            chars[chars.length-3] =c;
            chars[chars.length-2] ='0';
            chars[chars.length-1] ='0';
            }
			else if(num2==9){
            int num3 =Character.getNumericValue(chars[chars.length-4]);//takes the preceedig value
            if(num3<=8){
            num3++;
            char c = (char)(num3+'0');
            chars[chars.length-4] =c;
            chars[chars.length-3] ='0';
            chars[chars.length-2] ='0';
             chars[chars.length-1] ='0';
            }
        }
        }
        }
        String result ="";
        for(char rt: chars){
            result+=rt;
        }
        return result;
    }
    
    private void Users_update() {
        try {
            String sql = "select UserID, Type_name, Username,Password, IsActive  from users u1 inner join user_types u2 on u1.Type_ID = u2.Type_ID order by UserID";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            userAccounts_table.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
